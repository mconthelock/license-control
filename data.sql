  CREATE OR REPLACE FORCE VIEW "GPREPORT"."AYEARS" ("YEARS") AS 
  WITH period (start_year, end_year) as
(SELECT 2018, to_char(SYSDATE, 'yyyy')+1 FROM DUAL)

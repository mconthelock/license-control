  select "EMPCODE","LEAVE","A","ST" from
(
select EMPCODE, ETT_TYPE, LEAVE from VIEW_LR100P
)
pivot
(
 count(ETT_TYPE ) for ETT_TYPE in ('A' A, 'ST' ST)
)
